/** Modificando el caso de las letras */
let mensaje = "ALERTA! --- Este es un mensaje para TODOS los terricolas, estamos siendo invadidos --- ALERTA!";

console.log( mensaje );     // ALERTA! --- Este es un mensaje para TODOS los terricolas, estamos siendo invadidos --- ALERTA!

/** toLowerCase(): 
 * Convierte todas las letras a minúsculas */
console.log( mensaje.toLowerCase() );   // alerta! --- este es un mensaje para todos los terricolas, estamos siendo invadidos --- alerta!

/** toUpperCase(): 
 * Convierte todas las letras a mayúsculas. */
console.log( mensaje.toUpperCase() );   // ALERTA! --- ESTE ES UN MENSAJE PARA TODOS LOS TERRICOLAS, ESTAMOS SIENDO INVADIDOS --- ALERTA!