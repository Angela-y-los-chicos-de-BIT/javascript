/** Number.MAX_VALUE: 
 *  Representa el número más grande posible que JavaScript puede representar. */
console.log( Number.MAX_VALUE );

/** Number.MIN_VALUE: 
 *  Representa el número positivo más pequeño posible que JavaScript puede representar. */
console.log( Number.MIN_VALUE );

/** Number.POSITIVE_INFINITY: 
 *  Representa el infinito positivo. */
console.log( Number.POSITIVE_INFINITY );

/** Number.NEGATIVE_INFINITY: 
 *  Representa el infinito negativo. */
console.log( Number.NEGATIVE_INFINITY );

/** Number.NaN: 
 *  Representa "Not a Number" (no es un número). */
console.log( Number.NaN );


/** Number.MAX_SAFE_INTEGER:
 *  Representa el número entero más grande que JavaScript puede representar con precisión. 9007199254740991 (2^53 - 1) */
console.log( Number.MAX_SAFE_INTEGER );

/** Number.MIN_SAFE_INTEGER:
 *  Representa el número entero más pequeño que JavaScript puede representar con precisión. -9007199254740991 (-2^53 + 1) */
console.log( Number.MIN_SAFE_INTEGER );